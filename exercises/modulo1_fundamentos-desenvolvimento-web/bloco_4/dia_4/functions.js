// 1.Faça cinco programas, um para cada operação aritmética básica.
// Adição
function sum (a, b) {
  return a + b;
}
console.log(sum(2,10));

// subtração
function sub (a, b) {
  return a - b;
}
console.log(sub(2,10));

// multiplicação
function mul (a, b) {
  return a * b;
}
console.log(mul(2,10));

// divisão
function div (a, b) {
  return a / b;
}
console.log(div(2,10));

// módulo (resto)
function mod (a, b) {
  return a % b;
}
console.log(mod(2,10));
